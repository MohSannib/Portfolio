// --- DOM Elements & Safety Loading ---
document.addEventListener("DOMContentLoaded", () => {
    // 1. Dynamic Year with Safety Check
    const yearEl = document.querySelector('.year');
    if (yearEl) {
        yearEl.textContent = new Date().getFullYear();
    }

    const navbar = document.getElementById('navbar');
    const scrollTopBtn = document.getElementById('scrollTop');
    const sections = document.querySelectorAll('section');
    const navLinks = document.querySelectorAll('.nav-link');
    const menuBtn = document.getElementById('menu-btn');
    const navigation = document.getElementById('navigation');
    const langSelect = document.getElementById('langSelect');

    // 2. Navbar Scroll Effect & Scroll-to-Top Toggle
    window.addEventListener('scroll', () => {
        if (navbar && scrollTopBtn) {
            if (window.scrollY > 50) {
                navbar.classList.add('scrolled');
                scrollTopBtn.classList.add('active');
            } else {
                navbar.classList.remove('scrolled');
                scrollTopBtn.classList.remove('active');
            }
        }
        
        let current = '';
        sections.forEach(section => {
            if (window.scrollY >= section.offsetTop - 100) {
                current = section.getAttribute('id');
            }
        });

        // Exact match instead of includes()
        if (current) {
            navLinks.forEach(link => {
                link.classList.remove('active');
                if (link.getAttribute('href') === `#${current}`) {
                    link.classList.add('active');
                }
            });
        }
    });

    // 3. Mobile Menu Handlers
    if (menuBtn && navigation) {
        menuBtn.addEventListener('click', () => {
            navigation.classList.toggle('active');
            menuBtn.innerHTML = navigation.classList.contains('active') 
                ? '<i class="fas fa-times"></i>' 
                : '<i class="fas fa-bars"></i>';
        });
    }

    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            if (navigation && menuBtn) {
                navigation.classList.remove('active');
                menuBtn.innerHTML = '<i class="fas fa-bars"></i>';
            }
        });
    });

    // Mobile Menu Auto-Close on Resize Width
    window.addEventListener('resize', () => {
        if (window.innerWidth > 768 && navigation && menuBtn) {
            if (navigation.classList.contains('active')) {
                navigation.classList.remove('active');
                menuBtn.innerHTML = '<i class="fas fa-bars"></i>';
            }
        }
    });

    // 4. Scroll Reveal
    const revealElements = document.querySelectorAll('.reveal');
    const revealOnScroll = () => {
        revealElements.forEach(element => {
            if (element.getBoundingClientRect().top < window.innerHeight - 150) {
                element.classList.add('active');
            }
        });
    }
    window.addEventListener('scroll', revealOnScroll);
    revealOnScroll(); // init on load

    // 5. Robust Multilingual & RTL Support
    const state = { lang: 'en' };
    
    try {
        state.lang = localStorage.getItem('lang') || 'en';
    } catch (err) {
        console.warn('localStorage is restricted. Defaulting to english.');
    }

    function setLanguage(lang) {
        if (!translations[lang]) return; // safety catch invalid languages

        state.lang = lang;
        if (langSelect) langSelect.value = lang;

        try {
            localStorage.setItem('lang', lang);
        } catch (e) {
            // ignore localStorage warning on restricted browsers
        }

        document.documentElement.lang = lang;
        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
        }

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                // Keep innerHTML specifically to execute <span> and <strong>
                el.innerHTML = translations[lang][key]; 
            }
        });
    }

    if (langSelect) {
        langSelect.addEventListener('change', (e) => setLanguage(e.target.value));
    }
    
    // Finalize initialization
    setLanguage(state.lang);
});
